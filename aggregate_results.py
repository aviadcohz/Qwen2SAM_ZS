#!/usr/bin/env python3
"""Aggregate per-cell result JSONs into a single NeurIPS-style table.

Reads `results/<model>/<dataset>.json` files produced by the eval_*.py
scripts (each has a `summary` block with mIoU / pIoU / ARI / coverage) and
prints both a Markdown table and a LaTeX-ready `tabular` snippet.

Typical usage:
    python aggregate_results.py
    python aggregate_results.py --models sam3_vanilla grounded_sam3 qwen2sam_zs
    python aggregate_results.py --csv out.csv --latex out.tex
"""

from __future__ import annotations

import argparse
import csv
import json
import sys
from pathlib import Path

import yaml


_SUITE_ROOT = Path(__file__).resolve().parent
RESULTS_ROOT = _SUITE_ROOT / "results"
CONFIG_PATH = _SUITE_ROOT / "datasets_config.yaml"


METRIC_KEYS = [
    ("mIoU",     "matched_mean_iou"),
    ("pIoU",     "panoptic_iou"),
    ("Dice",     "panoptic_dice"),
    ("ARI",      "mean_ari"),
    ("Coverage", "mean_bg_coverage"),
]


def load_cell(path: Path) -> dict | None:
    if not path.exists():
        return None
    try:
        return json.loads(path.read_text())
    except Exception as e:      # noqa: BLE001
        print(f"[warn] failed to parse {path}: {e}", file=sys.stderr)
        return None


def collect(models: list[str], datasets: list[str]) -> dict:
    """grid[model][dataset] → summary dict (or None if missing).

    New layout : results/<model>/<dataset>/zero_shot_results.json
    Legacy flat: results/<model>/<dataset>.json (still read as fallback).
    """
    grid = {m: {} for m in models}
    for m in models:
        for d in datasets:
            new_path = RESULTS_ROOT / m / d / "zero_shot_results.json"
            legacy_path = RESULTS_ROOT / m / f"{d}.json"
            path = new_path if new_path.exists() else legacy_path
            grid[m][d] = load_cell(path)
    return grid


def fmt(val, digits: int = 4) -> str:
    if val is None:
        return "—"
    try:
        return f"{float(val):.{digits}f}"
    except Exception:           # noqa: BLE001
        return str(val)


def cell_metric(cell_json: dict | None, key: str):
    if cell_json is None:
        return None
    summary = cell_json.get("summary") or {}
    return summary.get(key)


def render_markdown(grid: dict, models: list[str], datasets: list[str],
                    metric_key: str, metric_label: str) -> str:
    lines = [
        f"### {metric_label}",
        "",
        "| Model / Dataset | " + " | ".join(datasets) + " | Mean |",
        "|" + "---|" * (len(datasets) + 2),
    ]
    for m in models:
        row_vals = [cell_metric(grid[m][d], metric_key) for d in datasets]
        nums = [v for v in row_vals if v is not None]
        mean = (sum(nums) / len(nums)) if nums else None
        cells = [fmt(v) for v in row_vals]
        lines.append(f"| {m} | " + " | ".join(cells) + f" | {fmt(mean)} |")
    return "\n".join(lines) + "\n"


def render_latex(grid: dict, models: list[str], datasets: list[str],
                 metric_key: str, metric_label: str) -> str:
    cols = "l" + "c" * (len(datasets) + 1)
    header = (
        f"% {metric_label} — generated by aggregate_results.py\n"
        f"\\begin{{tabular}}{{{cols}}}\n"
        f"\\toprule\nModel & " + " & ".join(datasets) + " & Mean \\\\ \n"
        "\\midrule\n"
    )
    rows = []
    for m in models:
        row_vals = [cell_metric(grid[m][d], metric_key) for d in datasets]
        nums = [v for v in row_vals if v is not None]
        mean = (sum(nums) / len(nums)) if nums else None
        cells = [fmt(v) for v in row_vals]
        rows.append(
            m.replace("_", "\\_") + " & " + " & ".join(cells)
            + f" & {fmt(mean)} \\\\"
        )
    footer = "\n\\bottomrule\n\\end{tabular}\n"
    return header + "\n".join(rows) + footer


def write_csv(path: Path, grid: dict, models: list[str],
              datasets: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["model", "dataset"]
                   + [k for k, _ in METRIC_KEYS]
                   + ["n_ok", "n_total"])
        for m in models:
            for d in datasets:
                cell = grid[m][d]
                summary = (cell or {}).get("summary", {}) if cell else {}
                row = [m, d] + [fmt(summary.get(key)) for _, key in METRIC_KEYS]
                row += [summary.get("n_ok", ""), summary.get("n_total", "")]
                w.writerow(row)


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--config", type=Path, default=CONFIG_PATH)
    parser.add_argument("--models", nargs="*", default=None,
                        help="Override models (default: from config).")
    parser.add_argument("--datasets", nargs="*", default=None,
                        help="Override datasets (default: from config).")
    parser.add_argument("--metric", type=str, default="matched_mean_iou",
                        choices=[k for _, k in METRIC_KEYS],
                        help="Which metric to format as the main table.")
    parser.add_argument("--csv", type=Path, default=None,
                        help="Also write a wide-form CSV with all metrics.")
    parser.add_argument("--latex", type=Path, default=None,
                        help="Also write a LaTeX tabular snippet (--metric).")
    args = parser.parse_args()

    with open(args.config) as f:
        cfg = yaml.safe_load(f)
    models = args.models or [m["name"] for m in cfg["models"]]
    datasets = args.datasets or list(cfg["datasets"].keys())

    grid = collect(models, datasets)

    # Main metric — Markdown table to stdout.
    metric_label = next(label for label, key in METRIC_KEYS if key == args.metric)
    md = render_markdown(grid, models, datasets, args.metric, metric_label)
    print(md)

    # Secondary view — all metrics stacked.
    print("\n" + "=" * 60 + "\n  ALL METRICS\n" + "=" * 60)
    for label, key in METRIC_KEYS:
        if key == args.metric:
            continue
        print(render_markdown(grid, models, datasets, key, label))

    if args.csv is not None:
        write_csv(args.csv, grid, models, datasets)
        print(f"[csv]    wrote {args.csv}")
    if args.latex is not None:
        args.latex.parent.mkdir(parents=True, exist_ok=True)
        args.latex.write_text(
            render_latex(grid, models, datasets, args.metric, metric_label)
        )
        print(f"[latex]  wrote {args.latex}")


if __name__ == "__main__":
    main()
